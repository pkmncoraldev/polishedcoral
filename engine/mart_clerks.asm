MartClerks::
	ld a, [wCurMoveNum]
	ld hl, MartClerkPics
	ld e, a
	ld d, 0
	add hl, de
	add hl, de
	ld c, [hl]
	ld e, 1
	ld d, 0
	add hl, de
	ld a, [hl]
	push af
	ld a, c
	ld bc, 20 tiles
	ld hl, RegisterGFX
	ld e, BANK(RegisterGFX)
	rst AddNTimes
	ld b, e
	ld c, 20
	ld d, h
	ld e, l
	ld hl, VTiles2 tile $50
	call Get2bpp
	pop af
	ld [wPlaceBallsY], a
	ret
	
